(function(){
  'use strict';
  const openBtn = document.getElementById('openLibraryBtn');
  const modal = document.getElementById('documentLibrary');
  const closeBtn = document.getElementById('closeLibraryBtn');
  const search = document.getElementById('documentSearch');
  const filters = document.getElementById('documentFilters');
  const list = document.getElementById('documentList');
  const count = document.getElementById('documentCount');
  let activeCategory = 'Tous';

  function esc(value){
    return String(value == null ? '' : value).replace(/[&<>"']/g, char => ({
      '&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'
    }[char]));
  }

  function formatSize(bytes){
    if(bytes < 1024 * 1024) return Math.round(bytes / 1024) + ' Ko';
    return (bytes / (1024 * 1024)).toFixed(1).replace('.', ',') + ' Mo';
  }

  function renderFilters(){
    const categories = ['Tous', ...new Set(window.DOCUMENT_LIBRARY.map(d => d.category))];
    filters.innerHTML = categories.map(cat =>
      `<button type="button" class="doc-filter ${cat===activeCategory?'is-active':''}" data-category="${esc(cat)}">${esc(cat)}</button>`
    ).join('');
    filters.querySelectorAll('.doc-filter').forEach(btn => btn.addEventListener('click', () => {
      activeCategory = btn.dataset.category;
      renderFilters();
      renderDocuments();
    }));
  }

  function renderDocuments(){
    const query = search.value.trim().toLocaleLowerCase('fr');
    const docs = window.DOCUMENT_LIBRARY.filter(doc => {
      const categoryOk = activeCategory === 'Tous' || doc.category === activeCategory;
      const haystack = `${doc.title} ${doc.description} ${doc.category}`.toLocaleLowerCase('fr');
      return categoryOk && (!query || haystack.includes(query));
    });
    count.textContent = `${docs.length} document${docs.length>1?'s':''}`;
    list.innerHTML = docs.length ? docs.map(doc => `
      <article class="document-card">
        <div class="document-card__icon">📄</div>
        <div class="document-card__body">
          <span class="document-card__category">${esc(doc.category)}</span>
          <h3>${esc(doc.title)}</h3>
          <p>${esc(doc.description)}</p>
          <small>${formatSize(doc.size)}</small>
        </div>
        <div class="document-card__actions">
          <a href="${encodeURI(doc.file)}" target="_blank" rel="noopener">Ouvrir le PDF</a>
          <a href="${encodeURI(doc.file)}" download>Télécharger</a>
        </div>
      </article>
    `).join('') : '<p class="document-empty">Aucun document ne correspond à cette recherche.</p>';
  }

  function openLibrary(){
    modal.classList.remove('hidden');
    document.body.classList.add('modal-open');
    modal.setAttribute('aria-hidden','false');
    setTimeout(() => search.focus(), 0);
  }
  function closeLibrary(){
    modal.classList.add('hidden');
    document.body.classList.remove('modal-open');
    modal.setAttribute('aria-hidden','true');
    openBtn.focus();
  }

  openBtn.addEventListener('click', openLibrary);
  const openTopBtn=document.getElementById('openLibraryTopBtn');
  if(openTopBtn)openTopBtn.addEventListener('click',openLibrary);
  closeBtn.addEventListener('click', closeLibrary);
  modal.addEventListener('click', e => {
    if(e.target === modal) closeLibrary();
  });
  document.addEventListener('keydown', e => {
    if(e.key === 'Escape' && !modal.classList.contains('hidden')) closeLibrary();
  });
  search.addEventListener('input', renderDocuments);

  renderFilters();
  renderDocuments();
})();
